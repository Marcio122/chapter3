# orginizing lists by sorting on alphabetical order
cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort()
print(cars)
# organizing lists by sorting on reverse order
cars.sort(reverse=True)
print(cars)

# sorting a list with sort function
cars = ['bmw', 'audi', 'toyota', 'subaru']
print(sorted(cars))
print(cars)

# printing a list on reverse
cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.reverse()
print(cars)

# finding the lenght of a list
cars = ['bmw', 'audi', 'toyota', 'subaru']
print(len(cars))